int AUDIO_buffer_isEmpty() {
	if(AUDIO_buffer_in == AUDIO_buffer_out) {
		return 1;
	} else {
		return 0;
	}
}

int AUDIO_buffer_isFull() {
	if(((AUDIO_buffer_in + 1) % AUDIO_buffer_size) == AUDIO_buffer_out) {
		return 1;
	} else {
		return 0;
	}
}

int AUDIO_buffer_push(int value) {
	// push a value to the buffer. accepted inputs (right now) should be a pwm value between 0 and 127
	if(AUDIO_buffer_isFull()) {
		return -1; // failed
	} else {
		AUDIO_buffer[AUDIO_buffer_in] = value;
		AUDIO_buffer_in = (AUDIO_buffer_in + 1) % AUDIO_buffer_size;
		return 1; // not failed
	}
}

int AUDIO_buffer_pop() {
	// Returns a value that theoretically should be between 0 and 127 (maybe i'll do 256 for more quantization, dunno);
	// this value represents the duty cycle of the audio, and is essentially the amplitude of the waveform at whatever sample,
	// quantized between 0 and 128
	int value = 0;
	if(AUDIO_buffer_isEmpty()) {
		return -1; // rip in peace
	} else {
		value = AUDIO_buffer[AUDIO_buffer_out];
		AUDIO_buffer_out = (AUDIO_buffer_out + 1) % AUDIO_buffer_size;
		return value;
	}
}
